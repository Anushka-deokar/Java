public class Student {
  private String name;
  private int age;

  // getter for name
  public String getName() {
    return name;
  }

  // setter for name
  public void setName(String newName) {
    name = newName;
  }

  // getter for age
  public int getAge() {
    return age;
  }

  // setter for age
  public void setAge(int newAge) {
    age = newAge;
  }
}
